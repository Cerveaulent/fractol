#include "libft.h"

char		*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	if (!(sub = (char *)malloc(len + 1)))
		return (NULL);
	while (s[start] && i < len)
		(sub[i++]) = s[start++];
	sub[i] = '\0';
	return (sub);
}
